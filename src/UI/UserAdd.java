    private void CheckAvailabilityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckAvailabilityActionPerformed
        try {
            String UserID = Email.getText();

            Connection con = ConnectionProvider.getInstance().getDBConnection();
            Statement st = con.createStatement();
            String query = "SELECT * FROM user WHERE email ='" + UserID + "'";
            ResultSet rs = st.executeQuery(query);

            if (rs.next()) {
                Commonlbl.setForeground(Color.red);
                Commonlbl.setText("User Email is not Available");
            } else {
                Commonlbl.setForeground(new Color(0, 51, 51));
                Commonlbl.setText("User Email is Available");
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_CheckAvailabilityActionPerformed

