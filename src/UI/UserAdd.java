    private void AddProfilePicBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddProfilePicBtnActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));

        FileNameExtensionFilter fileFilter = new FileNameExtensionFilter("*.Images", "jpg", "png");
        chooser.addChoosableFileFilter(fileFilter);

        int fileState = chooser.showSaveDialog(null);

        if (fileState == JFileChooser.APPROVE_OPTION) {
            File f = chooser.getSelectedFile();
            String path = f.getAbsolutePath();
            ImagePth = path;

            //display img
            ProfilePicImg.setIcon(resizePic(path));
        } else if (fileState == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(null, "Please Add Profile Picture");
        }
    }//GEN-LAST:event_AddProfilePicBtnActionPerformed

    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
        String UserID = Email.getText();
        String fname = FirstName.getText();
        String lname = LastName.getText();
        String password = String.valueOf(Password.getPassword());
        String rep_password = String.valueOf(RepPassword.getPassword());
        String position = String.valueOf(Position.getSelectedItem());
        String address = Address.getText();
        String question = String.valueOf(SecurityQuestion.getSelectedItem());
        String answer = QuestionAnswer.getText();
        String contactno = ContactNo.getText();

        if (uid_result) {
            JOptionPane.showMessageDialog(null, "User Email is Invalid");
        }

        if (fname_result) {
            JOptionPane.showMessageDialog(null, "First Name must have Alphabet Characters Only");
        }

        if (lname_result) {
            JOptionPane.showMessageDialog(null, "Last Name must have Alphabet Characters Only");
        }

        if (password_result) {
            JOptionPane.showMessageDialog(null, "Password required at least 1 uppercase and 1 lowercase,1 digit, 1 symbol(@#$%^&+=),length from 8 to 20");
        }

        if (!rep_password.equals(password)) {
            JOptionPane.showMessageDialog(null, "Password does not match");
        }

        if (position.equalsIgnoreCase("Please select a Position")) {
            JOptionPane.showMessageDialog(null, "Please select a Position");
        }

        if (contact_result) {
            JOptionPane.showMessageDialog(null, "Contact Number is Invalid");
        }

        if (address_result) {
            JOptionPane.showMessageDialog(null, "Enter Valid Address");
        }

        if (question.equalsIgnoreCase("Please select a Security Question")) {
            JOptionPane.showMessageDialog(null, "Please select a Security Question");
        }

        if (ImagePth == null) {
            JOptionPane.showMessageDialog(null, "Please Add Profile Picture");
        }

        if (UserID.isEmpty() || fname.isEmpty() || lname.isEmpty() || answer.isEmpty() || address.isEmpty() || contactno.isEmpty() || password.isEmpty() || rep_password.isEmpty()) {
            empty_result = true;
            JOptionPane.showMessageDialog(null, "Empty Input Field");
        } else {
            empty_result = false;
        }

        if (empty_result == false && address_result == false && uid_result == false && lname_result == false && fname_result == false && password_result == false && contact_result == false && rep_password_result == false && ImagePth != null && answer.isEmpty() == false) {
            try {

                Connection con = ConnectionProvider.getInstance().getDBConnection();
                String query = "INSERT INTO user(email,firstname,lastname,password,profilepic,position,contactno,question,answer,address) VALUES(?,?,?,?,?,?,?,?,?,?)";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1, UserID);
                ps.setString(2, fname);
                ps.setString(3, lname);
                ps.setString(4, password);
                ps.setString(5, ImagePth);
                ps.setString(6, position);
                ps.setString(7, contactno);
                ps.setString(8, question);
                ps.setString(9, answer);
                ps.setString(10, address);

                if (ps.executeUpdate() != 0) {
                    JOptionPane.showMessageDialog(null, "User Account Created");
                } else {
                    JOptionPane.showMessageDialog(null, "Error : DataBase Connectivity Problem");
                }
            } catch (com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(null, "User Email is not Available");
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_RegisterBtnActionPerformed

