    private void PasswordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PasswordKeyReleased
        String password = String.valueOf(Password.getPassword());
        int pass_length = password.length();

        String PATTERN = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+=])(?=\\S+$).{8,20}$";
        Pattern pat = Pattern.compile(PATTERN);
        Matcher match = pat.matcher(password);

        password_result = !match.matches();

        if (password_result) {
            Commonlbl.setForeground(Color.red);
            Commonlbl.setText("<html>Password required at least 1 uppercase, <br> 1 lowercase,1 digit and 1 symbol(@#$%^&+=),<br>length from 8 to 20</html>");
        } else {
            Commonlbl.setText(null);
        }
    }//GEN-LAST:event_PasswordKeyReleased

