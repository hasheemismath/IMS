    private void sendMailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendMailActionPerformed
        cusID = CustomerArrearList().get(selectedRow).getCustomerid();
        String sum1, sum2, Fullname = null;
        Double totpaidAmt, totpurchaseAmt, Arrear = 0.0;

        try {
            Connection con = ConnectionProvider.getInstance().getDBConnection();
            String query = "SELECT c.email,c.firstname,c.lastname,SUM(p.amountpaid),SUM(p.totalamount) FROM paymentc p, customer c WHERE c.customerid=p.customerid AND c.customerid= '" + cusID + "'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);

            if (rs.next()) {

                sum1 = rs.getString(4);
                totpaidAmt = Double.parseDouble(sum1);

                sum2 = rs.getString(5);
                totpurchaseAmt = Double.parseDouble(sum2);

                Arrear = totpurchaseAmt - totpaidAmt;

                Fullname = rs.getString("firstname") + " " + rs.getString("lastname");

                toEmail = rs.getString("email");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        String body = "Dear " + Fullname + ",\n\nThis mail is about your Payment Arrear at New Airtel Electronic Store."
                + "\n\nYour Payment Arrear Amount = Rs." + Arrear + "\n\nCould you please let me know when I can expect payment? "
                + "If payment has already been made please disregard this email."
                + "\n\nThank You !!!\nNew Airtel Electronic Store";

        System.out.println("SSLEmail Start");
        Properties props = new Properties();
        props.put("mail.smtp.host", "smtp.gmail.com"); //SMTP Host
        props.put("mail.smtp.socketFactory.port", "465"); //SSL Port
        props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory"); //SSL Factory Class
        props.put("mail.smtp.auth", "true"); //Enabling SMTP Authentication
        props.put("mail.smtp.port", "465"); //SMTP Port

        Authenticator auth = new Authenticator() {
            //override the getPasswordAuthentication method
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(AdminData.fromEmail, AdminData.password);
            }
        };

        Session session = Session.getDefaultInstance(props, auth);
        System.out.println("Session created");

        if (toEmail != null) {
            for (int i = 1; i <= 10; i++) {
            }
            sendEmail(session, toEmail, AdminData.payArrSubjet, body);
        } else {
            JOptionPane.showMessageDialog(null, "<html>Customer not found,<br>Please Select customer");
        }
    }//GEN-LAST:event_sendMailActionPerformed

