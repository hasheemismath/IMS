    public ArrayList<CustomerPaymentArrear> CustomerArrearList() {
        ArrayList<CustomerPaymentArrear> ilist = new ArrayList<>();

        try {
            Connection con = ConnectionProvider.getInstance().getDBConnection();
            String query = "SELECT p.customerid,c.email,c.firstname,c.lastname,SUM(p.amountpaid),SUM(p.totalamount) FROM paymentc p, customer c WHERE c.customerid=p.customerid GROUP BY p.customerid,c.firstname,c.lastname,c.email ";

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            CustomerPaymentArrear i;

            while (rs.next()) {
                String sum1 = rs.getString(5);
                Double totpaidAmt = Double.parseDouble(sum1);

                String sum2 = rs.getString(6);
                Double totpurchaseAmt = Double.parseDouble(sum2);

                Double Arrear = totpurchaseAmt - totpaidAmt;

                String Fullname = rs.getString("firstname") + " " + rs.getString("lastname");
                if (Arrear > 0) {
                    i = new CustomerPaymentArrear(rs.getInt("customerid"), Fullname, rs.getString("email"), totpaidAmt, totpurchaseAmt, Arrear);
                    ilist.add(i);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return ilist;
    }

    public void loadCustomerArrear() {
        ArrayList<CustomerPaymentArrear> list = CustomerArrearList();
        DefaultTableModel dtm = (DefaultTableModel) ArrearTable.getModel();

        for (int i = 0; i < list.size(); i++) {
            col[0] = list.get(i).getCustomerid();
            col[1] = list.get(i).getCustomername();
            col[2] = list.get(i).getEmail();
            col[3] = list.get(i).getTotalamount();
            col[4] = list.get(i).getAmountpaid();
            col[5] = list.get(i).getArrear();

            dtm.addRow(col);
        }
    }

