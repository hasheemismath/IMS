    private void BtnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCreateActionPerformed
        String name = NameText.getText();
        String type = TypeCombo.getSelectedItem().toString();
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        //DatePicker.setFormats(dateFormat);
        String date = dateFormat.format(DatePicker.getDate());
        String amount = AmountText.getText();

        try{
            Connection con = ConnectionProvider.getInstance().getDBConnection();
            String query = "insert into petty (expensename, expensetype, date, amount)values(?,?,?,?)";
            PreparedStatement myStat = con.prepareStatement(query);
            myStat.setString(1, name);
            myStat.setString(2, type);
            myStat.setString(3, date);
            myStat.setString(4, amount);

            int a = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this new entry?");
            if(a == JOptionPane.YES_OPTION){
                Vlis vli = new Vlis(name, date, amount);
                if(vli.ExUDval()){
                    myStat.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Successfully Added!");
                    new PCD().setVisible(true);
                    this.dispose();
                }
                //jLabel1.setText(date);
            }
        }catch(Exception e){
            e.printStackTrace();
        }

    }//GEN-LAST:event_BtnCreateActionPerformed

