    private void loadCustomerDetails()
    {
        
        try
        {
            Connection con = ConnectionProvider.getInstance().getDBConnection();
            Statement st = con.createStatement();
            String query = "SELECT * FROM customer WHERE customerid='"+SalesFinal.id+"'";
            ResultSet rs = st.executeQuery(query);
            while(rs.next())
            {
                lblCustomerID.setText(rs.getString("customerid"));
                lblCustomerName.setText(rs.getString("firstname")+" "+rs.getString("lastname"));
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Error occured, "+ex,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void loadCustomerArrears()
    {
        double total=0,bal=0;
        try
        {
            Connection con = ConnectionProvider.getInstance().getDBConnection();
            Statement st = con.createStatement();
            String query = "SELECT * FROM customer c, paymentc pc WHERE  c.customerid=pc.customerid AND pc.customerid='"+SalesFinal.id+"'";
            ResultSet rs = st.executeQuery(query);
            while(rs.next())
            {
                lblTotalSales.setText(rs.getDouble("totalamount")+" "+rs.getDouble("amountpaid"));
                total = total + rs.getDouble("totalamount");
                bal = bal + rs.getDouble("amountpaid");
                
                lblArrears.setText(String.valueOf(total-bal));
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Error occured, "+ex,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private int generateID()
    {
        String query = "SELECT * FROM paymentc";
        String rowName = "paymentid";
        
        return (int) queryExecution.generateID(query, rowName);
    }

